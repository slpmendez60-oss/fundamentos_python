# Convertidor de kilómetros a millas
km = 12.25
millas = km * 0.621371
print(km, "km son", millas, "millas")

# Convertidor de Celsius a Fahrenheit
celsius = 100.0
fahrenheit = (celsius * 9 / 5) + 32
print(celsius, "°C son", fahrenheit, "°F")

# Convertidor de horas a segundos
horas = 2
minutos = horas * 60
segundos = minutos * 60
print(horas, "hora(s) =", minutos, "minutos =", segundos, "segundos")
