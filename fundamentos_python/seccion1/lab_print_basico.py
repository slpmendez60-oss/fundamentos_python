# Mi primer programa en Python
print("¡Hola, Mundo!")

# Usando print() con multiples lineas
print("La Witsi Witsi Araña subio a su telaraña.")
print("Vino la lluvia y se la llevo.")

# print() vacio genera una linea en blanco
print("La Witsi Witsi Araña subio a su telaraña.")
print()
print("Vino la lluvia y se la llevo.")

# Usando el caracter de escape \n para nueva linea
print("La Witsi Witsi Araña\nsubio a su telaraña.")
print()
print("Vino la lluvia\ny se la llevo.")
